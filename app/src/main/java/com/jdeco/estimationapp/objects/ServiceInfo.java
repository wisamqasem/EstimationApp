package com.jdeco.estimationapp.objects;

public class ServiceInfo {
    String appId;
    String phase;
    String phase_type;
    String service_class_id;
    String service_class;
    String comp_id;
    String usage_type;
    String meter_type;


    public String getAppId() {
        return appId;
    }

    public void setAppId(String appId) {
        this.appId = appId;
    }

    public String getPhase() {
        return phase;
    }

    public void setPhase(String phase) {
        this.phase = phase;
    }

    public String getPhase_type() {
        return phase_type;
    }

    public void setPhase_type(String phase_type) {
        this.phase_type = phase_type;
    }

    public String getService_class_id() {
        return service_class_id;
    }

    public void setService_class_id(String service_class_id) {
        this.service_class_id = service_class_id;
    }

    public String getService_class() {
        return service_class;
    }

    public void setService_class(String service_class) {
        this.service_class = service_class;
    }

    public String getComp_id() {
        return comp_id;
    }

    public void setComp_id(String comp_id) {
        this.comp_id = comp_id;
    }

    public String getUsage_type() {
        return usage_type;
    }

    public void setUsage_type(String usage_type) {
        this.usage_type = usage_type;
    }

    public String getMeter_type() {
        return meter_type;
    }

    public void setMeter_type(String meter_type) {
        this.meter_type = meter_type;
    }
}
